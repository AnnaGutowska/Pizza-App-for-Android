package com.example.pizzaapp.backend;

/**
 The Topping class contains the  enum type which is a special data type that
 enables for a variable to be a set of predefined constants. In our case, it
 is the various toppings that can go on a pizza
 */
public enum Topping {
    Chicken,
    Beef,
    BlackOlives,
    Sausage,
    GreenPepper,
    Onion,
    Pepperoni,
    Mushroom,
    Pineapple,
    Ham,
    Cheese
}